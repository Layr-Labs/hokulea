/// Primitive 2^n-th roots of unity as `substrate-bn` `Fr` elements, lazily constructed.
pub static PRIMITIVE_ROOTS_OF_UNITY: Lazy<[Fr; 29]> = Lazy::new(|| {
    let mut out = [Fr::zero(); 29];
    for (slot, dec) in out.iter_mut().zip(PRIMITIVE_ROOTS_OF_UNITY_DEC.iter()) {
        *slot = Fr::from_str(dec).expect("valid decimal Fr literal");
    }
    out
});

/// EigenDA mainnet `[τ]G2`. See `rust-kzg-bn254-primitives::consts::G2_TAU` for the source values.
pub static G2_TAU: Lazy<G2> = Lazy::new(|| {
    // Fq2 layout in substrate-bn is `(real, imaginary)` (i.e. `c0 + c1·i`).
    let x_c0 = Fq::from_str(
        "19394299006376106554626551996044114846855237028623244664226757033024550999552",
    )
    .expect("valid x.c0");
    let x_c1 = Fq::from_str(
        "10478571113809844268398751534081669357808742555529167819607714577862447855483",
    )
    .expect("valid x.c1");
    let y_c0 = Fq::from_str(
        "9205262336805673656533560220225620941045451042642528799409071118332922267006",
    )
    .expect("valid y.c0");
    let y_c1 = Fq::from_str(
        "10552783866161062341197740743287753408530108186218052255509661543860392060676",
    )
    .expect("valid y.c1");
    let x = Fq2::new(x_c0, x_c1);
    let y = Fq2::new(y_c0, y_c1);
    // The constant is built unchecked, mirroring the arkworks `MontFp!` /
    // `G2Affine::new_unchecked` definition in the reference crate.
    let affine = AffineG2::new_unchecked(x, y);
    affine.into()
});

/// Helper: get a clone of the `n`-th primitive root of unity (i.e. of order `2^n`).
pub fn get_primitive_root_of_unity(power: usize) -> Option<Fr> {
    PRIMITIVE_ROOTS_OF_UNITY.get(power).copied()
}

/// Helper: collect the entire primitive-roots table into a `Vec`.
pub fn primitive_roots_of_unity() -> Vec<Fr> {
    PRIMITIVE_ROOTS_OF_UNITY.iter().copied().collect()
}
